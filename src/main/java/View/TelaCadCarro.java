    private void iniciarBotoes() {
        btSalvar.addActionListener(e -> {
            try {
                controller.salvarCarro();
            } catch (EstacionamentoException ex) {
                exibirMensagem(ex.getMessage());
            }
        });
    }

    public void exibirMensagem(String carro_salvo) {
        JOptionPane.showMessageDialog(null, carro_salvo);
    }
